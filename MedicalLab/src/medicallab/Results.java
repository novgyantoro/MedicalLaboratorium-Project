    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
       if (TCodeTb.getText().isEmpty() || ResNumTb.getText().isEmpty() || CostTb.getText().isEmpty() || ResTb.getText().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Missing Data!!!");
} else {
    try { 
        Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/medicallabdb", "root", "");
        var Add = Con.prepareStatement("insert into resulttbl values(?,?,?,?,?,?)");

        Add.setInt(1, Integer.valueOf(ResNumTb.getText()));
        Add.setInt(2, Integer.valueOf(PatIdCb.getSelectedItem().toString()));
        Add.setInt(3, Integer.valueOf(TCodeTb.getText()));
        
        String resText = ResTb.getText().trim(); 
        if (resText.isEmpty()) {
            
            Add.setString(4, ""); 
        } else {
            Add.setString(4, resText);
        }
        
        Add.setString(5, ResDateTb.getDate().toString().substring(4, 10) + ResDateTb.getDate().toString().substring(24, 28));
        Add.setInt(6, Integer.valueOf(CostTb.getText()));
        int row = Add.executeUpdate();
        JOptionPane.showMessageDialog(this, "Results Added Successfully");
        ShowResults();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }
    }//GEN-LAST:event_jButton1MouseClicked

